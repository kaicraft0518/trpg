/**
 * 技能名からカテゴリを返す
 * @param {string} name - 技能名
 * @returns {string} カテゴリ名
 */
export function getSkillCategory(name) {
  for (const [cat, list] of Object.entries(SKILL_CATEGORIES)) {
    if (list.some(s => name === s || name.startsWith(s))) return cat;
  }
  return 'その他';
}
